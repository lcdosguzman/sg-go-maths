package sggomaths

// Suma retorna la suma de dos números enteros
func Suma(num1, num2 int) int {
	return num1 + num2
}

// Resta retorna la Resta de dos números enteros
func Resta(num1, num2 int) int {
	return num1 - num2
}

// Multiplica retorna la multiplicacion de dos números enteros
func Multiplica(num1, num2 int) int {
	return num1 * num2
}

// Divide retorna la división de dos números enteros
func Divide(num1, num2 int) int {
	return num1 / num2
}
